package com.code.entity;

import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import jakarta.validation.constraints.NotNull;

@Entity
@Table(name = "package_details")
public class PackageDetails {

	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	private int packageId;

	@NotNull(message = "Package name cannot be blank")
	private String packageName;

	private String packageDescription;

	@NotNull(message = "Package type cannot be blank")
	private String packageType;

	@NotNull(message = "Package cost cannot be blank")
	private double packageCost;

	@NotNull(message = "Hotel name cannot be blank")
	private String hotelName;

	public int getPackageId() {
		return packageId;
	}

	public void setPackageId(int packageId) {
		this.packageId = packageId;
	}

	public String getPackageName() {
		return packageName;
	}

	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}

	public String getPackageDescription() {
		return packageDescription;
	}

	public void setPackageDescription(String packageDescription) {
		this.packageDescription = packageDescription;
	}

	public String getPackageType() {
		return packageType;
	}

	public void setPackageType(String packageType) {
		this.packageType = packageType;
	}

	public double getPackageCost() {
		return packageCost;
	}

	public void setPackageCost(double packageCost) {
		this.packageCost = packageCost;
	}

	public String getHotelName() {
		return hotelName;
	}

	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}

	public PackageDetails() {
		super();
	}

}